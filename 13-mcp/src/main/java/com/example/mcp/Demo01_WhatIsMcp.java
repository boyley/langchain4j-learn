package com.example.mcp;

/**
 * Demo01: 什么是 MCP (Model Context Protocol)
 *
 * MCP 是 Anthropic 提出的开放协议，解决的核心问题：
 * 让 AI 模型能够安全、标准化地与外部工具和数据交互
 */
public class Demo01_WhatIsMcp {

    public static void main(String[] args) {
        System.out.println("""
                ╔══════════════════════════════════════════════════════════════╗
                ║              MCP (Model Context Protocol) 简介                ║
                ╚══════════════════════════════════════════════════════════════╝

                【一句话解释】
                MCP = AI 模型调用外部工具的"USB接口标准"

                【为什么需要 MCP？】

                传统方式的问题：
                ┌─────────────────────────────────────────────────────────────┐
                │  App A ──→ 自己写代码连接数据库                               │
                │  App B ──→ 自己写代码调用 API                                │
                │  App C ──→ 自己写代码操作文件                                │
                │                                                             │
                │  问题：每个应用都要重复造轮子，没有标准                         │
                └─────────────────────────────────────────────────────────────┘

                MCP 的解决方案：
                ┌─────────────────────────────────────────────────────────────┐
                │                                                             │
                │  ┌─────┐      ┌─────────────┐      ┌────────────────┐       │
                │  │App A│      │             │      │ DB Server      │       │
                │  ├─────┤ MCP  │   MCP       │ MCP  ├────────────────┤       │
                │  │App B│←────→│   协议       │←────→│ API Server     │       │
                │  ├─────┤      │             │      ├────────────────┤       │
                │  │App C│      │             │      │ File Server    │       │
                │  └─────┘      └─────────────┘      └────────────────┘       │
                │                                                             │
                │  优点：标准协议，任意客户端可以连接任意服务端                    │
                └─────────────────────────────────────────────────────────────┘

                【MCP 的三大核心能力】

                1. Tools（工具）- 让 AI 能执行操作
                   ├─ 查询数据库
                   ├─ 发送邮件
                   ├─ 操作文件
                   └─ 调用 API

                2. Resources（资源）- 让 AI 能读取数据
                   ├─ 文件内容
                   ├─ 数据库记录
                   └─ 网页内容

                3. Prompts（提示模板）- 预定义的交互模式
                   ├─ 代码审查模板
                   ├─ 翻译模板
                   └─ 分析模板

                【工作流程】

                用户: "帮我查一下北京的天气"
                         │
                         ▼
                ┌─────────────────┐
                │    AI 模型      │  分析：需要调用天气工具
                └────────┬────────┘
                         │ MCP 请求
                         ▼
                ┌─────────────────┐
                │  MCP Server     │  执行：调用天气 API
                │  (天气工具)      │
                └────────┬────────┘
                         │ 返回结果
                         ▼
                ┌─────────────────┐
                │    AI 模型      │  整合：生成自然语言回复
                └────────┬────────┘
                         │
                         ▼
                用户: "北京今天晴，气温 25°C..."

                【与 Function Calling 的区别】

                Function Calling:
                - 工具定义写死在代码里
                - 换个工具要改代码、重新部署
                - 不同模型厂商的实现不同

                MCP:
                - 工具由独立的 Server 提供
                - 添加新工具只需连接新的 Server
                - 标准协议，一次实现到处运行

                【常见的 MCP Server】

                官方提供:
                - filesystem: 文件操作
                - github: GitHub 操作
                - postgres: 数据库查询
                - puppeteer: 浏览器自动化
                - slack: Slack 消息

                社区提供:
                - 各种 API 封装
                - 企业内部系统集成
                - 自定义工具

                ═══════════════════════════════════════════════════════════════
                接下来的 Demo 将演示如何实现和使用 MCP
                ═══════════════════════════════════════════════════════════════
                """);
    }
}
